#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*result;
	size_t	i;

	i = 0;
	len = ft_strlen(s1);
	result = (char *)malloc(sizeof(char) * (len + 1));
	if (!result)
		return (NULL);
	while (i < len)
	{
		result[i] = s1[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
